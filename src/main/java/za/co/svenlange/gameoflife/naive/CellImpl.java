package za.co.svenlange.gameoflife.naive;

import za.co.svenlange.gameoflife.Cell;

public class CellImpl implements Cell {

    private final int x;
    private final int y;

    public CellImpl(int x, int y) {
        this.x = x;
        this.y = y;
    }

    @Override
    public int getX() {
        return x;
    }

    @Override
    public int getY() {
        return y;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;

        Cell cell = (Cell) o;

        if (x != cell.getX()) return false;
        if (y != cell.getY()) return false;

        return true;
    }

    @Override
    public int hashCode() {
        int result = x;
        result = 31 * result + y;
        return result;
    }
}
